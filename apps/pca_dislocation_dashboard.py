def find_repo_root(start: Path | None = None) -> Path:
    """Find the repository root from this file path or the current working tree."""
    current = (start or Path(__file__)).resolve()
    if current.is_file():
        current = current.parent

    for candidate in [current, *current.parents]:
        if (candidate / ".git").exists() and (candidate / "results").exists():
            return candidate
    return Path(__file__).resolve().parents[1]


def validate_columns(
    frame: pd.DataFrame, required_columns: set[str], artifact_name: str
) -> list[str]:
    """Return validation errors for required columns without raising tracebacks."""
    missing = sorted(required_columns - set(frame.columns))
    if not missing:
        return []
    return [f"{artifact_name} is missing required columns: {', '.join(missing)}"]


def load_artifacts(repo_root: Path | None = None) -> dict[str, pd.DataFrame | list[str]]:
    """Load Notebook 07b artifacts and collect user-facing validation errors."""
    root = repo_root or find_repo_root()
    base = root / ARTIFACT_DIR
    artifacts: dict[str, pd.DataFrame | list[str]] = {"_errors": []}
    errors: list[str] = []

    for key, filename in ARTIFACT_FILES.items():
        path = base / filename
        if not path.exists():
            errors.append(f"Missing required artifact: {path.relative_to(root)}")
            artifacts[key] = pd.DataFrame()
            continue

        try:
            frame = pd.read_csv(path)
        except Exception as exc:  # pragma: no cover - depends on local file damage
            errors.append(f"Could not read {path.relative_to(root)}: {exc}")
            artifacts[key] = pd.DataFrame()
            continue

        if "date" in frame.columns:
            frame["date"] = pd.to_datetime(frame["date"], errors="coerce")
        if key in REQUIRED_COLUMNS:
            errors.extend(validate_columns(frame, REQUIRED_COLUMNS[key], filename))
        artifacts[key] = frame

    artifacts["_errors"] = errors
    return artifacts


def latest_top_abs_dislocation_assets(scorecard: pd.DataFrame, n: int = 5) -> list[str]:
    """Select default assets from the latest scorecard by absolute dislocation."""
    if scorecard.empty or not {"asset", "pca_dislocation_z"}.issubset(scorecard.columns):
        return []

    frame = scorecard.copy()
    frame["_abs_dislocation"] = frame["pca_dislocation_z"].abs()
    return (
        frame.sort_values("_abs_dislocation", ascending=False)["asset"]
        .dropna()
        .astype(str)
        .head(n)
        .tolist()
    )


