def filter_scorecard(
    scorecard: pd.DataFrame,
    asset_classes: list[str] | None,
    regimes: list[str] | None,
    diagnostic_labels: list[str] | None,
) -> pd.DataFrame:
    """Apply viewer filters to the latest scorecard artifact."""
    frame = scorecard.copy()
    if frame.empty:
        return frame

    if asset_classes and "asset_class" in frame.columns:
        frame = frame[frame["asset_class"].astype(str).isin(asset_classes)]
    if regimes and "dominant_regime" in frame.columns:
        frame = frame[frame["dominant_regime"].astype(str).isin(regimes)]
    if diagnostic_labels and "diagnostic_label" in frame.columns:
        frame = frame[frame["diagnostic_label"].astype(str).isin(diagnostic_labels)]
    return frame


def _select_top_abs(frame: pd.DataFrame, top_n: int) -> pd.DataFrame:
    if frame.empty or "pca_dislocation_z" not in frame.columns:
        return frame
    selected = frame.assign(_abs_dislocation=frame["pca_dislocation_z"].abs())
    return selected.sort_values("_abs_dislocation", ascending=False).head(top_n)


def make_dislocation_bar(scorecard: pd.DataFrame, top_n: int) -> go.Figure:
    """Build latest PCA dislocation bar chart from the scorecard artifact."""
    frame = _select_top_abs(scorecard, top_n)
    title = "Latest PCA Dislocation Z-Scores"
    if frame.empty:
        return _empty_figure(title, "No scorecard rows match the current filters.")

    frame = frame.copy()
    frame["_direction"] = frame["pca_dislocation_z"].apply(
        lambda value: "Positive dislocation"
        if value >= 2
        else "Negative dislocation"
        if value <= -2
        else "Diagnostic only"
    )
    color_map = {
        "Positive dislocation": PALETTE["blue"],
        "Negative dislocation": PALETTE["rose"],
        "Diagnostic only": PALETTE["muted_slate"],
    }

    fig = px.bar(
        frame.sort_values("pca_dislocation_z", ascending=True),
        x="asset",
        y="pca_dislocation_z",
        color="_direction",
        hover_data=[col for col in SCORECARD_COLUMNS if col in frame.columns],
        title=title,
        template=NEUTRAL_TEMPLATE,
        color_discrete_map=color_map,
    )
    fig.add_hline(y=2, line_dash="dash", line_color=PALETTE["slate"], opacity=0.62)
    fig.add_hline(y=-2, line_dash="dash", line_color=PALETTE["slate"], opacity=0.62)
    fig.update_traces(
        marker_line_width=0,
        hovertemplate="<b>%{x}</b><br>PCA dislocation z-score: %{y:.2f}<extra></extra>",
    )
    fig.update_layout(
        legend_title_text="",
        bargap=0.24,
    )
    fig.update_xaxes(title_text="", tickangle=-35)
    fig.update_yaxes(title_text="PCA dislocation z-score", zeroline=True)
    return _apply_chart_layout(fig, height=440, margin={"l": 54, "r": 24, "t": 62, "b": 104})


