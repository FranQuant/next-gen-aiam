@app.callback(
    Output("scorecard-table-container", "children"),
    Output("dislocation-bar", "figure"),
    Output("dislocation-momentum-scatter", "figure"),
    Input("asset-class-filter", "value"),
    Input("regime-filter", "value"),
    Input("diagnostic-filter", "value"),
    Input("top-n-filter", "value"),
)
def update_scorecard_views(
    asset_classes: list[str] | str | None,
    regimes: list[str] | str | None,
    diagnostic_labels: list[str] | str | None,
    top_n: int,
) -> tuple[dash_table.DataTable, go.Figure, go.Figure]:
    scorecard = ARTIFACTS.get("scorecard", pd.DataFrame())
    assert isinstance(scorecard, pd.DataFrame)
    filtered = filter_scorecard(
        scorecard,
        _normalize_filter(asset_classes),
        _normalize_filter(regimes),
        _normalize_filter(diagnostic_labels),
    )
    return (
        _scorecard_table(filtered),
        make_dislocation_bar(filtered, int(top_n or 10)),
        make_dislocation_momentum_scatter(filtered),
    )


@app.callback(
    Output("dislocation-timeseries", "figure"),
    Input("asset-selector", "value"),
)
def update_dislocation_timeseries(selected_assets: list[str] | str | None) -> go.Figure:
    dislocation_scores = ARTIFACTS.get("dislocation_scores", pd.DataFrame())
    assert isinstance(dislocation_scores, pd.DataFrame)
    if isinstance(selected_assets, str):
        selected_assets = [selected_assets]
    return make_dislocation_timeseries(dislocation_scores, selected_assets or [])


