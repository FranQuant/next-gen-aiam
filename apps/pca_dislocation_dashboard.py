def make_rolling_pca_diagnostics(diagnostics: pd.DataFrame) -> go.Figure:
    """Build rolling PCA diagnostics from Notebook 07b reconstruction artifacts."""
    title = "Rolling PCA Reconstruction Diagnostics"
    if diagnostics.empty or "date" not in diagnostics.columns:
        return _empty_figure(title, "Rolling PCA diagnostic artifact is unavailable.")

    cumulative_col = "pc1_pc5_cumulative_explained_variance_ratio"
    error_cols = [
        col
        for col in ["reconstruction_rmse", "reconstruction_mae", "reconstruction_error"]
        if col in diagnostics.columns
    ]
    has_cumulative = cumulative_col in diagnostics.columns
    if not has_cumulative and not error_cols:
        return _empty_figure(title, "No supported diagnostic series are present.")

    frame = diagnostics.sort_values("date").copy()
    fig = go.Figure()
    if has_cumulative:
        fig.add_trace(
            go.Scatter(
                x=frame["date"],
                y=frame[cumulative_col],
                mode="lines",
                name="PC1-PC5 cumulative explained variance",
                line={"color": PALETTE["blue"], "width": 2},
                yaxis="y",
            )
        )
    for idx, col in enumerate(error_cols):
        fig.add_trace(
            go.Scatter(
                x=frame["date"],
                y=frame[col],
                mode="lines",
                name=col.replace("_", " ").title(),
                line={
                    "color": [PALETTE["muted_slate"], PALETTE["teal"], PALETTE["gold"]][
                        idx % 3
                    ],
                    "width": 1.8,
                },
                yaxis="y2" if has_cumulative else "y",
            )
        )

    layout = {
        "title": title,
        "xaxis": {"title": ""},
    }
    if has_cumulative and error_cols:
        layout.update(
            {
                "yaxis": {"title": "Cumulative explained variance"},
                "yaxis2": {
                    "title": "Reconstruction error",
                    "overlaying": "y",
                    "side": "right",
                    "showgrid": False,
                },
            }
        )
    else:
        layout["yaxis"] = {
            "title": "Cumulative explained variance"
            if has_cumulative
            else "Reconstruction error"
        }
    fig.update_layout(**layout)
    return _apply_chart_layout(
        fig,
        height=470,
        margin={"l": 58, "r": 72, "t": 62, "b": 68},
        legend={"y": -0.22, "x": 0, "xanchor": "left", "yanchor": "top"},
    )


def _data_table_columns(frame: pd.DataFrame) -> list[dict[str, object]]:
    numeric_cols = set(frame.select_dtypes(include="number").columns)
    columns: list[dict[str, object]] = []
    for col in frame.columns:
        column: dict[str, object] = {"name": col, "id": col}
        if col in numeric_cols:
            column.update({"type": "numeric", "format": {"specifier": ".2f"}})
        columns.append(column)
    return columns


